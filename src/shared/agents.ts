// ─── Agent definitions ───
//
// Rax used to ship with free-form tabs (user could spawn N claude sessions and
// rename them). The multi-agent model replaces that with a fixed roster: five
// named "agents", each bound to one persistent claude session. Tabs ARE
// agents now; there is no add/remove.
//
// The dock window (see src/renderer/dock) renders these five identities as a
// vertical glass dock parked on the left edge of the user's primary display.
// Selecting an agent in the dock routes focus to that agent's tab in pill /
// fullscreen. The voice orb on its own surface coordinates the roster — it
// can dispatch a turn to a specific agent, and emits a completion toast when
// that agent's run finishes.

export interface AgentDefinition {
  /** Stable tab id used across renderer / main / persisted state. Reserved
   *  string — never collide with a real claude tab id generated by main. */
  id: string
  /** Display name shown in the dock tooltip, sidebar row, and toasts. */
  name: string
  /** One-line tagline displayed under the name on hover. Pure flavor. */
  tagline: string
  /** Theme accent for status pulses, hover halo, active indicator pill.
   *  Both light and dark modes pick the same hex — the dock surface is
   *  always dark-glass so contrast holds. */
  accent: string
  /** Soft accent variant used for status backgrounds and glow shadows. */
  accentSoft: string
  /** Single character/emoji rendered as the agent's icon glyph in the
   *  dock and sidebar. Cheap, scales perfectly, no extra asset pipeline.
   *  Pick visually distinct shapes so the roster is recognizable at the
   *  60×60 dock size. */
  glyph: string
}

