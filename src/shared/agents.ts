/** Quick lookup by id. Built once at module load. */
const AGENT_BY_ID: Map<string, AgentDefinition> = new Map(AGENTS.map((a) => [a.id, a]))

export function getAgent(id: string): AgentDefinition | undefined {
  return AGENT_BY_ID.get(id)
}

export function isAgentId(id: string | undefined | null): boolean {
  return !!id && AGENT_BY_ID.has(id)
}

